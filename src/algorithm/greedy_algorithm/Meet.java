package algorithm.greedy_algorithm;

public class Meet {
    int begin;
    int end;

    public Meet(int begin, int end) {
        this.begin = begin;
        this.end = end;
    }

    /**
     * @return the begin
     */
    public int getBegin() {
        return begin;
    }

    /**
     * @return the end
     */
    public int getEnd() {
        return end;
    }
}